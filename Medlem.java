public class Medlem {
    private String navn;
    private int telefonNummer;
    private String adresse;
    private String email;
    private int alder;

    public Medlem(String navn, int telefonNummer, String adresse, String email, int alder) {
        this.navn = navn;
        this.telefonNummer = telefonNummer;
        this.adresse = adresse;
        this.email = email;
        this.alder = alder;
    }
}
